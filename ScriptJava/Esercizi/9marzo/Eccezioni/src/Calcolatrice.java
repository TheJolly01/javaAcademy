public class Calcolatrice {

    double[] risultati = {0, 0, 0, 0};

    public static double addizione(double add1, double add2){

        return add1 + add2;
    }

    public static double moltiplicazione(double fatt1, double fatt2){

        return fatt1 + fatt2;
    }

    public static double sottrazione(double minuendo, double sottraendo){

        return minuendo - sottraendo;
    }



}
